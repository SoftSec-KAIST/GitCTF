if [ "$#" -lt 1 ]; then
    echo "Usage: $0 [dir]"
    exit 1
fi
GITDIR=$1
git -C $GITDIR fetch --prune
### Assume that problems are in branches other than master.
git -C $GITDIR for-each-ref refs/remotes | cut -d/ -f4 \
    | grep -v master | grep -v HEAD
